//***去掉字符串左边空格***
function strltrim(str)
{
  for(i=0;i<str.length;i++)
  {
     if(str.substr(i,1)!=" ")
     {
        break;
     }
  }
  return(str.substring(i,str.length));
}

//***去掉字符串右边空格
function strrtrim(str)
{
  for(i=str.length-1;i>=0;i--)
  {
     if(str.substr(i,1)!=" ")
     {
        break;
     }
  }
  return(str.substring(0,i+1));
}

//***去掉字符串右边与左边空格***
function strtrim(str)
{
  str1=strltrim(str);
  str1=strrtrim(str1);
  return(str1);
}

